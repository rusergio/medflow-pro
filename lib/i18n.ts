export function t(locale: Locale, key: string): string {
  return translations[locale]?.[key] ?? translations.generic[key] ?? key;
}

export function getLocaleFromStorage(): Locale {
  try {
    const s = localStorage.getItem('medflow_locale');
    if (s === 'generic' || s === 'pt-PT') return s;
  } catch {}
  return 'generic';
}

export function setLocale(locale: Locale) {
  try {
    localStorage.setItem('medflow_locale', locale);
  } catch {}
}
